/**
 * Operadores aritméticos de atribuição 
 */

 console.log("Operadores de Atribuição")
 console.log("+= | soma e atribuição a variável")
 console.log("-= | subtração e atribuição a variável")
 console.log("*= | multiplicação e atribuição a variável")
 console.log("/= | divisão e atribuição a variável")
 console.log("Exemplos:")
 let x = 10
 console.log("x = 10")
 console.log("10 += 5 = " + (x += 5))
 console.log("15 -= 5 = " + (x -= 5))
 console.log("10 *= 5 = " + (x *= 5))
 console.log("50 /= 5 = " + (x /= 5))
 

 